public class do_while_loop {
    public static void main(String[] args) {
        int a = 4;
        do {
            System.out.println("Smaller");
            a++;
        } while (a < 8);
    }

}

/*
 * The main difference between a while and do while loop is that do while loop
 * runs and then checks the statement but while
 * loop checks the statement first and then runs
 */