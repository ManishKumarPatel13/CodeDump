// Types of inheritence

// 1. single inheritence - a derived class with only one base class is know as single inheritence

// 2. multiple inheritence - a derived class with more then one base class 

// 3. hierarchical inheritence - several derived class from a single base class

// 4. multilevel inheritence - when a class is derived from a derived class (A-->B-->C)

// 5. Hybrid inheritence - it is a combination of multiple and multilevel inheritence

// example of hybrid inheritence - A --> B in this B and C are made form A and D is made from B and C
//                                 |     |
//                                 C --> D

